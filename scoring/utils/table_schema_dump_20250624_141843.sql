DROP TABLE IF EXISTS `answer_category_result`;
CREATE TABLE `answer_category_result` (
  `ANS_CAT_RESULT_ID` bigint(20) NOT NULL AUTO_INCREMENT,
  `EVAL_CAT_CD` varchar(20) NOT NULL,
  `ANS_SCORE_ID` bigint(20) NOT NULL,
  `ANS_CAT_SCORE` float DEFAULT NULL,
  `STRENGTH_KEYWORD` text DEFAULT NULL,
  `WEAKNESS_KEYWORD` text DEFAULT NULL,
  `RGS_DTM` timestamp NULL DEFAULT NULL,
  `UPD_DTM` timestamp NULL DEFAULT current_timestamp(),
  PRIMARY KEY (`ANS_CAT_RESULT_ID`),
  KEY `EVAL_CAT_CD` (`EVAL_CAT_CD`),
  KEY `ANS_SCORE_ID` (`ANS_SCORE_ID`),
  CONSTRAINT `answer_category_result_ibfk_1` FOREIGN KEY (`EVAL_CAT_CD`) REFERENCES `evaluation_category` (`EVAL_CAT_CD`),
  CONSTRAINT `answer_category_result_ibfk_2` FOREIGN KEY (`ANS_SCORE_ID`) REFERENCES `answer_score` (`ANS_SCORE_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=2074 DEFAULT CHARSET=utf8mb3 COLLATE=utf8mb3_general_ci;

DROP TABLE IF EXISTS `answer_score`;
CREATE TABLE `answer_score` (
  `ANS_SCORE_ID` bigint(20) NOT NULL AUTO_INCREMENT,
  `INTV_ANS_ID` bigint(20) NOT NULL,
  `ANS_SUMMARY` text DEFAULT NULL,
  `EVAL_SUMMARY` text DEFAULT NULL,
  `INCOMPLETE_ANSWER` tinyint(1) DEFAULT NULL,
  `INSUFFICIENT_CONTENT` tinyint(1) DEFAULT NULL,
  `SUSPECTED_COPYING` tinyint(1) DEFAULT NULL,
  `SUSPECTED_IMPERSONATION` tinyint(1) DEFAULT NULL,
  `RGS_DTM` timestamp NULL DEFAULT NULL,
  `UPD_DTM` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`ANS_SCORE_ID`),
  KEY `INTV_ANS_ID` (`INTV_ANS_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=641 DEFAULT CHARSET=utf8mb3 COLLATE=utf8mb3_general_ci;

